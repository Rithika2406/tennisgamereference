import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner S=new Scanner(System.in);
		int t=S.nextInt();
		while(t-->0)
		{
		    int a=S.nextInt();
		    int b=S.nextInt();
		    int c=S.nextInt();
		    int d=S.nextInt();
		    if(a==0 && b==0 && c==0 && d==0)
		    {
		        System.out.println("IN");
		    }
		    else{
		        System.out.println("OUT");
		    }
		}

	}
}
